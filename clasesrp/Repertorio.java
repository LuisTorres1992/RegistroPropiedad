package clasesrp;

/**
 *
 * @author Juancho
 */
public class Repertorio {
          public long id;
        public int repertorio;
        public int año;
        public String motivo;

    public Repertorio() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public int getRepertorio() {
        return repertorio;
    }

    public void setRepertorio(int repertorio) {
        this.repertorio = repertorio;
    }

    public int getAño() {
        return año;
    }

    public void setAño(int año) {
        this.año = año;
    }

    public String getMotivo() {
        return motivo;
    }

    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }

   
        
        
}
